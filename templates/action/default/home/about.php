<div class="text">
    <p>
        The whole point of this site is to help people search multiple <a href="http://www.craigslist.org">craigslist</a> locations
        at the same time. 
    </p>
    
    <p>
        I frequent a bunch of guitar &amp; bass-centric message boards and noticed that tons of people 
        <a href="http://www.ernieball.com/forums/music-man-basses/19489-ebay-craigslist-treasures.html">search "foreign" craigslist locations</a>
        to find good deals on used guitars and basses. Location doesn't matter &mdash; a deal's a deal.
        The thing is, with <a href="http://www.craigslist.org">craigslist</a>, you're using a location-based service. It's not really geared to help you 
        <a href="/instruments?q=Ernie+Ball+Stingray">find an Ernie Ball Stingray</a> in 
        <a href="/search/goto?section=instruments&s=1&q=Ernie+Ball+Stingray&sort=city&city=sfbay">San Francisco</a>, 
        <a href="/search/goto?section=instruments&s=1&q=Ernie+Ball+Stingray&sort=city&city=losangeles">Los Angeles</a> and 
        <a href="/search/goto?section=instruments&s=1&q=Ernie+Ball+Stingray&sort=city&city=newyork">New York</a> at the same time. 
        You'd have to search all the locations individually...
    </p>
    
    <p>
        That was the inspiration for this site. I just wanted to help my friends on the
        <a href="http://www.ernieball.com/forums/">Music Man Forums</a> by making their deal scavanging a bit easier.
    </p>
    
    <p>
        The original version of this site went live on www.phatduckk.com/clmm some time in 2007 and was dubbed "Phatducck's gear finder".
        The "clmm" in thr URL stood for "Craig's List Music Man". 
        It only searched for instruments and the only people I told about it were my friends on the 
        <a href="http://www.ernieball.com/forums/">Music Man Forums</a>. 
        Eventually folks on other guitar boards found out about it &amp; started using it.
    </p>
    
    <p>
        After 5 or 6 months I got requests to add support for cars... then boats. 
        I added those sections and started seeing a bunch of referrers in my logs like the 
        <a href="http://www.ramchargercentral.com">Mopar truck forums</a>,
        <a href="http://dieseltruckresource.com/">Diesel Truck Resource</a> &amp; other unexpected sources.
    </p>    
    
    <p>
        So, after seeing that a bunch of people were finding the site usefull I thought I'd give it a decent UI, 
        get a little mascot dude, add all the other "for sale" sections, put it on its own domain... and, voila, Craig's Little Buddy was born. 
    </p>
    
    <p>
        Now <a href="/search?q=shit">go find some shit</a> =).
    </p>

